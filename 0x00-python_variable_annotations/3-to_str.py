#!/usr/bin/env python3
"""
A module containing a function to convert a float to its string representation

This module defines a type-annotated function `to_str` that takes a float
as an argument and returns its string representation.
"""


def to_str(n: float) -> str:
    """
    Convert a float to its string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the input float.
    """
    return str(n)
