#!/usr/bin/env python3
"""
A module containing a function to add two float numbers.

This module defines a type-annotated function `add` that takes two
floating-point numbers as arguments and returns their sum.
"""


def add(a: float, b: float) -> float:
    """
    Add two floats.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of the two float numbers.
    """
    return a + b
